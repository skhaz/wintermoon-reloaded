#include "Framework/Thread.h"
#include "Platforms/PC/ThreadPrivate.h"



WINTERMOON_BEGIN_NAMESPACE

Thread::Thread()
{
    d = new ThreadPrivate();
    d->setTarget(this);
}

Thread::~Thread()
{
    delete d;
}

void Thread::start()
{
    d->start();
}

void Thread::wait()
{
    d->wait();
}

